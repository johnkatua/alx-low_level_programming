#include "holberton.h"
/**
 * print_diagonal - It takes one argument
 * @n: length of line
 * draws a diagonal line
 *
 * Returns: returns void
 */
void print_diagonal(int n)
{
	int a, b;

	if (n <= 0)
	{
		_putchar('\n');
	}
	else
	{
		for (a = 0; a < n; a++)
		{
			for (b = 0; b < a; b++)
			{
				_putchar(' ');
			}
			_putchar('\\');
			_putchar('\n');
		}
	}
}
